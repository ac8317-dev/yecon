	public static boolean DrawBackCarLine(Canvas canvas, Paint paint,
			float LeftPt[][], float RightPt[][], int Color, Context context) {

		Path path = new Path();
		paint.setColor(Color);
		
		float ioffsetW = getW(context);
		float ioffsetH = getH(context);

		float xStart, yStart, xEnd, yEnd;
		for (int i = 0; i < LeftPt.length - 1; i++) {
			xStart = LeftPt[i][0]*ioffsetW; 
			yStart = LeftPt[i][1]*ioffsetH;
			xEnd = LeftPt[i + 1][0]*ioffsetW;
			yEnd = LeftPt[i + 1][1]*ioffsetH;

			path.moveTo(xStart, yStart);
			path.lineTo(xEnd, yEnd);
			canvas.drawPath(path, paint);
		}

		for (int i = 0; i < RightPt.length - 1; i++) {
			xStart = RightPt[i][0]*ioffsetW;
			yStart = RightPt[i][1]*ioffsetH;
			xEnd = RightPt[i + 1][0]*ioffsetW;
			yEnd = RightPt[i + 1][1]*ioffsetH;

			path.moveTo(xStart, yStart);
			path.lineTo(xEnd, yEnd);
			canvas.drawPath(path, paint);
		}
		return true;
	}

	public static boolean DrawBackCarTrack(Canvas canvas, Paint paint,
			int u1Angle, Context context) {

		boolean bLeft = false;
		if (u1Angle <= 0) {
			bLeft = true;
		}

		u1Angle %= 360;
		u1Angle = Math.abs(u1Angle);
		if (u1Angle > 40) {
			u1Angle = 40;
		}

		if (bLeft) {
			if (u1Angle > 32) // Red Track
			{
				DrawBackCarLine(canvas, paint, LineLeftA[u1Angle],
						LineLeftB[u1Angle], Color.RED, context);
			} else // Green Track
			{
				DrawBackCarLine(canvas, paint, LineLeftA[u1Angle],
						LineLeftB[u1Angle], Color.GREEN, context);
			}
		} else {
			if (u1Angle > 32) // Red Track
			{
				DrawBackCarLine(canvas, paint, LineRightA[u1Angle],
						LineRightB[u1Angle], Color.RED, context);
			} else // Green Track
			{
				DrawBackCarLine(canvas, paint, LineRightA[u1Angle],
						LineRightB[u1Angle], Color.GREEN, context);
			}
		}
		return true;
	}

	private static float getW(Context context){
		
		return context.getResources().getDimension(R.dimen.dync_track_w);
	}
	
	private static float getH(Context context){

		return context.getResources().getDimension(R.dimen.dync_track_h);
	}
